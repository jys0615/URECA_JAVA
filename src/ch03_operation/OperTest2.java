package ch03_operation;

public class OperTest2 {

	public static void main(String[] args) {

		int a = 123, b = 4;
		System.out.println(a);
		a += b; // a = a + b;
		System.out.println(a);
		a -= b; // a = a - b;
		System.out.println(a);
		a *= b; // a = a * b;
		System.out.println(a);
		a /= b; // a = a / b;
		System.out.println(a);
		a %= b; // a = a % b;
		System.out.println(a);
	}

}
